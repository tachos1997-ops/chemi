export const ELEMENT_METADATA = COMBOS.reduce((acc, combo) => {
  if (!acc[combo.result]) {
    acc[combo.result] = { age: combo.age };
  }
  combo.ingredients.forEach((ingredient) => {
    if (!acc[ingredient]) {
      acc[ingredient] = { age: 'Primal' };
    }
  });
  return acc;
}, {});

export function normaliseIngredients(a, b) {
  return [a, b].sort((x, y) => x.localeCompare(y));
}

export function findCombo(a, b) {
  const [first, second] = normaliseIngredients(a, b);
  return COMBOS.find((combo) => {
    const [i1, i2] = normaliseIngredients(combo.ingredients[0], combo.ingredients[1]);
    return i1 === first && i2 === second;
  }) || null;
}

export const AGE_THRESHOLDS = {
  Primal: 4,
  Natural: 10,
  Civilization: 20,
  Industrial: 35,
  Modern: 50,
  Futuristic: 70
};

export function getAgeUnlockThreshold(age) {
  return AGE_THRESHOLDS[age] ?? 4;
}
